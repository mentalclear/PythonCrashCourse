players = ['charles', 'martina', 'michael', 'florence', 'eli']
print(players[0:3])  # will not print the 3d element actually

print(players[1:4])

# this will work too, starts from the beginning of the list
print(players[:4])

# starts from the beginning of the list to the end.
print(players[2:])

# Only last 3 of the list
print(players[-3:])